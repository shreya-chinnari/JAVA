public class SumOfNumbers {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 5;
        
        // Calculate the sum
        int sum = num1 + num2;
        
        // Print the result
        System.out.println("Sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
